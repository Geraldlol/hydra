// Test-only stub for `require("vscode")`. The real vscode module is provided
// at runtime by the VS Code extension host; pure node:test runs have no
// such module. Pre-test, write a tiny stub into node_modules/vscode/ so any
// SUT that does `import * as vscode from "vscode"` resolves to the stub.
//
// Tests configure the stub via the two helpers it exports:
//   (vscode as any).__setMockConfig("hydraRoom.someKey", value);
//   (vscode as any).__clearMockConfig();
//
// .gitignore'd alongside node_modules, recreated on every `npm test` via the
// `pretest` script in package.json.

const fs = require("node:fs");
const path = require("node:path");

const targetDir = path.join(__dirname, "..", "node_modules", "vscode");
fs.mkdirSync(targetDir, { recursive: true });

fs.writeFileSync(
  path.join(targetDir, "package.json"),
  JSON.stringify({ name: "vscode", version: "0.0.0-stub", main: "index.js" }, null, 2) + "\n",
);

fs.writeFileSync(
  path.join(targetDir, "index.js"),
  `// Auto-generated by scripts/setup-vscode-stub.js — do not edit by hand.
// Minimal vscode stub for node:test runs outside the extension host.

// Tests can mutate currentConfig directly with bare keys (no "hydraRoom."
// prefix needed); section is ignored by the stub. This matches how the
// agentArgs / codex+claudeTransport tests were originally written.
const currentConfig = {};

function getConfiguration(_section) {
  return {
    get(key, defaultValue) {
      return key in currentConfig ? currentConfig[key] : defaultValue;
    },
    update() { return Promise.resolve(); },
    inspect() { return undefined; },
  };
}

module.exports = {
  workspace: {
    getConfiguration,
    workspaceFolders: undefined,
    onDidChangeConfiguration: () => ({ dispose() {} }),
  },
  window: {
    showQuickPick: async () => undefined,
    showInputBox: async () => undefined,
    showErrorMessage: async () => undefined,
    showWarningMessage: async () => undefined,
    showInformationMessage: async () => undefined,
  },
  commands: {
    executeCommand: async () => undefined,
  },
  ConfigurationTarget: {
    Global: 1,
    Workspace: 2,
    WorkspaceFolder: 3,
  },
  Uri: {
    file: (p) => ({ fsPath: p, toString: () => p }),
    joinPath: (base, ...parts) => ({ fsPath: [base.fsPath, ...parts].join("/"), toString: () => [base.fsPath, ...parts].join("/") }),
  },
  // Test helper: read/write the simulated workspace configuration. The bare
  // key (e.g. "codexModel") matches what tests originally used; section is
  // ignored by getConfiguration so this just works.
  currentConfig,
};
`,
);

console.log("vscode stub written to " + targetDir);
